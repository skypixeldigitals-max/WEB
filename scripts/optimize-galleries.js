// Optimizes EVERY photo in each property folder into images/gallery/<slug>/NN.jpg
// (1600px wide, q76, progressive) and emits js/gallery-data.js for the lightbox.
const sharp = require("sharp");
const fs = require("fs");
const path = require("path");

const ROOT = "C:/Users/admin/Downloads/CODE/shakir website images";
const OUT_ROOT = path.join(__dirname, "..", "images", "gallery");
const JS_OUT = path.join(__dirname, "..", "js");

// slug -> folder name (same properties as portfolio-manifest.json; Hero images excluded)
const PROPERTIES = [
  { slug: "aaathma", folder: "Aaathma" },
  { slug: "armitage-hill-galle", folder: "Armitage Hill Galle" },
  { slug: "artotel-sanur", folder: "Artotel Sanur" },
  { slug: "ayr-castle", folder: "Ayr Castle" },
  { slug: "cape-waligama", folder: "Cape Waligama" },
  { slug: "helios-boutique-villa", folder: "Helios Boutique Villa" },
  { slug: "kalkudah-beach-house", folder: "Kalkudah Beach House" },
  { slug: "landesi-galle", folder: "Landesi Galle" },
  { slug: "le-grand-galle", folder: "Le Grand Galle" },
  { slug: "leela-walauwwa", folder: "Leela Walauwwa" },
  { slug: "mayur-lodge", folder: "Mayur Lodge" },
  { slug: "nine-skies", folder: "Nine Skies" },
  { slug: "on-the-rocks", folder: "On The Rocks" },
  { slug: "rock-villa", folder: "Rock Villa" },
  { slug: "solar-crab", folder: "Solar Crab" },
  { slug: "sun-house", folder: "Sun House" },
  { slug: "why-house", folder: "Why House" },
];

(async () => {
  const data = {};
  let grandTotal = 0, grandBytes = 0;
  for (const p of PROPERTIES) {
    const srcDir = path.join(ROOT, p.folder);
    const outDir = path.join(OUT_ROOT, p.slug);
    fs.mkdirSync(outDir, { recursive: true });
    const files = fs.readdirSync(srcDir).filter(f => /\.(jpe?g|png)$/i.test(f)).sort();
    const images = [];
    let i = 0;
    for (const f of files) {
      i++;
      const outName = String(i).padStart(2, "0") + ".jpg";
      const info = await sharp(path.join(srcDir, f))
        .rotate()
        .resize({ width: 1600, withoutEnlargement: true })
        .jpeg({ quality: 76, progressive: true, mozjpeg: true })
        .toFile(path.join(outDir, outName));
      images.push(`images/gallery/${p.slug}/${outName}`);
      grandBytes += info.size;
    }
    grandTotal += images.length;
    data[p.slug] = { name: p.folder, images };
    console.log(p.slug.padEnd(24), images.length, "photos");
  }
  fs.mkdirSync(JS_OUT, { recursive: true });
  fs.writeFileSync(
    path.join(JS_OUT, "gallery-data.js"),
    "// Generated by scripts/optimize-galleries.js — do not edit by hand\nwindow.GALLERIES = " +
      JSON.stringify(data, null, 1) + ";\n"
  );
  console.log(`\nTotal: ${grandTotal} photos, ${Math.round(grandBytes / 1024 / 1024)} MB → js/gallery-data.js written`);
})();
